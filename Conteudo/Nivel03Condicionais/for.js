// Explicando o for
for( let i = 0; i <  5; i++) {
	if( i % 2 !== 0) { // todos numeros menor que 5 que não possui modulo de 2(impares)
	console.log(i);
	}
}

// While
let i = 5;

while( i >= 1) {
	if(i % 2 !== 0) {
		console.log(i);
	}
	i--;
}

